import Foundation

let firstCard = 11
let secondCard = 10

if firstCard + secondCard == 2 {
    print("Aces")
} else if firstCard + secondCard == 21 {
    print("Black Jack")
} else {
    print("Regular Card")
}

/*
 What's the difference between if & if-else ? The answer is if tells Swift to check the code and will execute the code if the condition is met, with else-if there's 2 different execution for whether or not the condition is true or false
